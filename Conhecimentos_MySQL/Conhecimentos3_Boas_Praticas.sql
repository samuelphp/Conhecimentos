"Conhecimentos 3 Boas práticas: 

* Definir nomes das tabelas com até 8 caracteres
	exemplo : COMCLIEN (nome relacionado com o sistema, no caso sistema comercial)

* Relacionar o tipo do MYSQL ao nome do campo
	exemplo

	C_ para campo do tipo caracteres
	D_ para campo do tipo data
	N_ para campo do tipo número
	B_ para campo do tipo boolean


 

Criar o banco de dados
Criar tabela, definindo nome abrindo parenteses colocando os nomes dos campos da tabela 
e os tipos com os valores de espaços fixos seguidos por virgula e finalizando com fecha parenteses 
e ponto e vírgula."


